package obje;

public class profesor {
    private String nyp;
    private Integer dni;
    private String mail;

    public String getNyp() {
        return nyp;
    }

    public void setNyp(String nyp) {
        this.nyp = nyp;
    }

    public Integer getDni() {
        return dni;
    }

    public void setDni(Integer dni) {
        this.dni = dni;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }
}
